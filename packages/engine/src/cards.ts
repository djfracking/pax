export type Coalition = "afghan" | "british" | "russian" | "none";
export type Suit = "political" | "intelligence" | "economic" | "military";
export type Region = "kabul" | "kandahar" | "punjab" | "herat" | "persia" | "transcaspia";

export type ActionIcon = "tax" | "gift" | "build" | "move" | "battle" | "betray" | "spy";
export type ImpactIcon = "spy" | "army" | "road" | "tribe" | "leverage" | "suit_political" | "suit_intelligence" | "suit_economic" | "suit_military";

export type CardEffectHook =
  | "on_play"
  | "on_buy"
  | "on_turn_start"
  | "on_battle"
  | "on_betray"
  | "on_dominance_check"
  | "battle_resolution"
  | "battle_protection"
  | "betray_protection"
  | "army_movement"
  | "spy_movement"
  | "play_card"
  | "purchase"
  | "build"
  | "tax"
  | "hostage"
  | "favored_suit"
  | "start_of_turn"
  | "dominance_check";

export interface CardEffect {
  id: string;
  hook: CardEffectHook;
  ruleText: string;
}

export interface CardDefinition {
  id: string;
  name: string;
  suit: Suit;
  region: Region;
  coalition: Coalition;
  rank: number;
  stars: number;
  isPatriot: boolean;
  actionIcons: ActionIcon[];
  impactIcons: ImpactIcon[];
  prize: Exclude<Coalition, "none"> | null;
  effects: CardEffect[];
}

export const REGION_BORDERS: Array<[Region, Region]> = [
  ["herat", "persia"],
  ["herat", "transcaspia"],
  ["herat", "kabul"],
  ["herat", "kandahar"],
  ["kabul", "transcaspia"],
  ["kabul", "kandahar"],
  ["kabul", "punjab"],
  ["kandahar", "punjab"],
  ["persia", "transcaspia"],
];

const borderSet = new Set<string>();
for (const [a, b] of REGION_BORDERS) {
  borderSet.add(`${a}:${b}`);
  borderSet.add(`${b}:${a}`);
}

export function areRegionsAdjacent(a: Region, b: Region): boolean {
  return borderSet.has(`${a}:${b}`);
}

export function getAdjacentRegions(region: Region): Region[] {
  const adjacent: Region[] = [];
  for (const [a, b] of REGION_BORDERS) {
    if (a === region) adjacent.push(b);
    else if (b === region) adjacent.push(a);
  }
  return adjacent;
}


